static int g_debug = 0;

#define DBG(...) do { if (g_debug) { fprintf(stderr, "[DEBUG] " __VA_ARGS__); } } while (0)

static void dbg_hexdump(const char *label, const unsigned char *buf, int len)
{
    if (!g_debug) return;
    fprintf(stderr, "[DEBUG] %s (%d bytes): ", label, len);
    for (int i = 0; i < len; i++) fprintf(stderr, "%02x ", buf[i]);
    fprintf(stderr, "\n");
}

static void dbg_sleep_ms(int ms)
{
    if (ms < 0) return;
    DBG("sleep(%d ms)\n", ms);
#ifdef _WIN32
    Sleep((DWORD)ms);
#else
    usleep((useconds_t)ms * 1000);
#endif
}

