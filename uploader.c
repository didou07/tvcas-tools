static void read_config_file(const char *path)
{
    unsigned char buf[90];
    FILE *f = fopen(path, "rb");
    if (!f) {
        printf("File not found!\n");
        exit(1);
    }
    size_t n = fread(buf, 1, sizeof(buf), f);
    fclose(f);
    if (n < 1 || buf[0] != 0x7d) {
        printf("File incorrect!\n");
        exit(1);
    }
    DBG("config file '%s' passed 0x7D marker check (%zu bytes read)\n", path, n);
}

static void run(const char *device_path, int query_only)
{
    open_port(device_path);
    configure_port(BAUD_HANDSHAKE);

    reset_card();
    printf("Waiting ATR: ");
    read_data(7);
    dbg_sleep_ms(50);

    if (rx[1] == 0x24) {
        printf("3B240030423030 (TVCAS smartcard)\n");
        printf("\nThis smartcard cannot be reprogrammed!\n");
        printf("It must first be cleaned with an ERASER-device.\n");
        printf("More: https://tvcas.com/#eraser\n");
        close_port();
        exit(1);
    } else if (rx[1] == 0x1f) {
        printf("3B1F9638050320061702044010FF07089000 (THC20F17BD-V40 blank)\n");
    } else {
        printf("unknown\n");
        close_port();
        exit(1);
    }

    if (query_only) {
        close_port();
        return;
    }

    {
        const unsigned char hs1[] = { 0x1a, 0x03, 0x00, 0x09 };
        write_chunk(hs1, sizeof(hs1));
    }
    read_data(4);
    read_data(9);
    putchar('0');
    fflush(stdout);

    close_port();
    open_port(device_path);
    read_data(11);
    close_port();
    dbg_sleep_ms(100);

    open_port(device_path);
    {
        const unsigned char hs2[] = { 0xff, 0x10, 0x96, 0x79 };
        write_chunk(hs2, sizeof(hs2));
    }
    read_data(4);
    read_data(4);
    close_port();
    dbg_sleep_ms(10);

    open_port(device_path);
    configure_port(BAUD_UPLOAD);
    printf("\nSwitch speed to 224000 baud (3,57 Mhz)...");
    printf("OK\nStart programming\n");

    {
        int off = 0;
        for (int i = 0; i < NCHUNKS; i++) {
            int len = CHUNK_LENS[i];
            write_chunk(&PAYLOAD[off], len);
            off += len;
            if (CHUNK_DELAYS_MS[i] >= 0) dbg_sleep_ms(CHUNK_DELAYS_MS[i]);
            printf(".");
            fflush(stdout);
        }
    }

    printf(" OK\n");
#ifdef _WIN32
    dbg_sleep_ms(200);
    configure_port(BAUD_HANDSHAKE);
    dbg_sleep_ms(100);
    configure_port(BAUD_HANDSHAKE);
    {
        const unsigned char final_hs[] = { 0x1a, 0x03, 0x00, 0x09 };
        write_chunk(final_hs, sizeof(final_hs));
    }
    printf("RESET CARD");
    printf("OK\n");
#else
    printf("RESET CARD");
    for (int i = 0; i < 3; i++) {
        reset_card();
    }
    printf("OK\n");
#endif
    close_port();
}

int main(int argc, char **argv)
{
    const char *port_arg = NULL;
    const char *config_path = NULL;

    for (int i = 1; i < argc; i++) {
        if (strcmp(argv[i], "-d") == 0) {
            g_debug = 1;
        } else if (!port_arg) {
            port_arg = argv[i];
        } else if (!config_path) {
            config_path = argv[i];
        } else {
            usage();
            return 1;
        }
    }

    if (!port_arg) {
        usage();
        return 1;
    }

    DBG("debug mode enabled\n");

    if (config_path) {
        read_config_file(config_path);
    }

#ifdef _WIN32
    char device_path[64];
    int looks_like_path = (strchr(port_arg, '\\') != NULL) ||
                           (strncmp(port_arg, "COM", 3) == 0) ||
                           (strncmp(port_arg, "com", 3) == 0);
    if (looks_like_path) {
        snprintf(device_path, sizeof(device_path), "%s", port_arg);
    } else {
        snprintf(device_path, sizeof(device_path), "\\\\.\\COM%s", port_arg);
    }
#else
    const char *device_path = port_arg;
#endif

    run(device_path, config_path == NULL);
    return 0;
}
